function replaceInterpolation(text: string, vars: Record<string, string>): string {
  return text.replace(/\{\{(\w+)\}\}/g, (_, key) => vars[key] ?? `{{${key}}}`);
}

export function getTranslation(locale: Locale, key: string, vars?: Record<string, string>): string {
  const text = translations[locale][key] ?? translations.en[key] ?? key;
  return vars ? replaceInterpolation(text, vars) : text;
}
